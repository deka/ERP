<template>
  <div class="ERP-container">
    <el-card class="box-card" style="margin-top: 10px;height: 60px" shadow="never">
      <el-row>
        <el-form ref="getemplist" :model="getemplist" label-width="100px" style="margin-top: -9px">
          <el-col :span="5" style="margin-left: 10px">
            <el-form-item label="状态">
              <el-select v-model="getemplist.stat" :value="getemplist.stat" clearable @keyup.enter.native="handleFilter">
                <el-option label="启用" value="1"/>
                <el-option label="停用" value="2"/>
              </el-select>
            </el-form-item>
          </el-col>
          <!--更多搜索条件-->
          <el-col :span="3" style="margin-left: 20px">
            <!-- 搜索按钮 -->
            <el-button v-waves class="filter-item" type="primary" icon="el-icon-search" style="width: 86px" round @click="handleFilter">{{ $t('public.search') }}</el-button>
          </el-col>
        </el-form>
      </el-row>
    </el-card>
    <el-card class="box-card" style="margin-top: 10px" shadow="never">
      <!-- 批量操作 -->
      <el-dropdown @command="handleCommand">
        <el-button v-waves class="filter-item" style="margin-left: 0" type="primary">
          {{ $t('public.batchoperation') }} <i class="el-icon-arrow-down el-icon--right"/>
        </el-button>
        <el-dropdown-menu slot="dropdown" style="width: 140px">
          <el-dropdown-item style="text-align: left" command="delete"><svg-icon icon-class="shanchu" style="width: 40px"/>{{ $t('public.delete') }}</el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
      <!-- 表格导出操作 -->
      <el-button v-waves :loading="downloadLoading" class="filter-item" style="width: 86px" @click="handleExport"> <svg-icon icon-class="daochu"/>{{ $t('public.export') }}</el-button>
      <!-- 打印操作 -->
      <el-button v-waves class="filter-item" icon="el-icon-printer" style="width: 86px" @click="handlePrint">{{ $t('public.print') }}</el-button>
      <!-- 新建操作 -->
      <el-button v-waves class="filter-item" icon="el-icon-plus" type="success" style="width: 86px" @click="handleAdd">{{ $t('public.add') }}</el-button>
      <el-dialog :visible.sync="categoryVisible" title="新建奖励规则设置" class="normal" width="600px" center>
        <el-form ref="addCategoryForm" :rules="addCategoryFormRules" :model="addCategoryForm" class="demo-ruleForm" style="margin: 0 auto; width: 400px">
          <el-form-item :label="$t('InstallmentrateList.advanceDay')" label-width="120px" prop="advanceDay">
            <el-input v-model="addCategoryForm.advanceDay" autocomplete="off"/>
          </el-form-item>
          <el-form-item :label="$t('InstallmentrateList.rewardMoney')" label-width="120px" prop="rewardMoney">
            <el-input v-model="addCategoryForm.rewardMoney" autocomplete="off"/>
          </el-form-item>
          <el-form-item :label="$t('InstallmentrateList.isEffective')" label-width="120px" prop="isEffective">
            <el-select v-model="addCategoryForm.isEffective" placeholder="请选择状态" style="width: 100%">
              <el-option label="启用" value="1"/>
              <el-option label="停用" value="2"/>
            </el-select>
          </el-form-item>
        </el-form>
        <span slot="footer" class="dialog-footer">
          <el-button type="primary" @click="handlesave()">保存</el-button>
          <el-button type="danger" @click="handlecancel()">取消</el-button>
        </span>
      </el-dialog>
    </el-card>

    <el-card class="box-card" style="margin-top: 10px" shadow="never">
      <!-- 列表开始 -->
      <el-table
        v-loading="listLoading"
        :key="tableKey"
        :data="list"
        border
        fit
        highlight-current-row
        style="width: 100%;"
        @selection-change="handleSelectionChange">
        <el-table-column
          type="selection"
          width="55"
          align="center"/>
        <el-table-column :label="$t('InstallmentrateList.id')" :resizable="false" align="center" min-width="100">
          <template slot-scope="scope">
            <span>{{ scope.row.id }}</span>
          </template>
        </el-table-column>
        <el-table-column :label="$t('InstallmentrateList.advanceDay')" :resizable="false" align="center" min-width="250">
          <template slot-scope="scope">
            <span>{{ scope.row.advanceDay }}</span>
          </template>
        </el-table-column>
        <el-table-column :label="$t('InstallmentrateList.rewardMoney')" :resizable="false" align="center" min-width="350">
          <template slot-scope="scope">
            <span>{{ scope.row.rewardMoney }}</span>
          </template>
        </el-table-column>
        <el-table-column :label="$t('InstallmentrateList.isEffective')" :resizable="false" align="center" min-width="250">
          <template slot-scope="scope">
            <span>{{ scope.row.isEffective | isEffectiveFilter }}</span>
          </template>
        </el-table-column>
        <el-table-column :label="$t('public.actions')" :resizable="false" align="center" min-width="230">
          <template slot-scope="scope">
            <el-button title="修改" type="primary" size="mini" icon="el-icon-edit" circle @click="handleEdit(scope.row)" />
            <el-button title="删除" size="mini" type="danger" icon="el-icon-delete" circle @click="handleDelete(scope.row)"/>
          </template>
        </el-table-column>
      </el-table>
      <!-- 列表结束 -->
      <pagination v-show="total>0" :total="total" :page.sync="getemplist.pagenum" :limit.sync="getemplist.pagesize" @pagination="getlist" />
      <!--修改开始=================================================-->
      <el-dialog :visible.sync="editcategoryVisible" title="修改奖励规则设置" class="normal" width="600px" center>
        <el-form ref="editCategoryForm" :rules="editCategoryFormRules" :model="editCategoryForm" class="demo-ruleForm" style="margin: 0 auto; width: 400px">
          <el-form-item :label="$t('InstallmentrateList.advanceDay')" label-width="120px" prop="advanceDay">
            <el-input v-model="editCategoryForm.advanceDay" autocomplete="off"/>
          </el-form-item>
          <el-form-item :label="$t('InstallmentrateList.rewardMoney')" label-width="120px" prop="rewardMoney">
            <el-input v-model="editCategoryForm.rewardMoney" autocomplete="off"/>
          </el-form-item>
          <el-form-item :label="$t('InstallmentrateList.isEffective')" label-width="120px" prop="isEffective">
            <el-select v-model="editCategoryForm.isEffective" placeholder="请选择状态" style="width: 100%">
              <el-option label="启用" value="1"/>
              <el-option label="停用" value="2"/>
            </el-select>
          </el-form-item>
        </el-form>
        <span slot="footer" class="dialog-footer">
          <el-button type="primary" @click="handleOk()">修改</el-button>
          <el-button type="danger" @click="handleNo()">取消</el-button>
        </span>
      </el-dialog>
      <!--修改结束=================================================-->
    </el-card>
  </div>
